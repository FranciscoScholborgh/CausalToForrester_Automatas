    private void import_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_import_btnMouseClicked
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        jfc.setDialogTitle("Importar diagrama causal: ");
	jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filter = new FileNameExtensionFilter(".CSL", "csl");
	jfc.addChoosableFileFilter(filter);
        int returnValue = jfc.showOpenDialog(null);
	// int returnValue = jfc.showSaveDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = jfc.getSelectedFile();
            System.out.println(selectedFile.getAbsolutePath());
        }
    }//GEN-LAST:event_import_btnMouseClicked

